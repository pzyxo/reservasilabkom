<script>
  function jadwal() {
        $.ajax({
            url: "<?= base_url('/explore/jadwal/') ?>",
            dataType: "json",
            success: function(response) {
                $('#viewmodal').html(response.data).show();
                $('#jadwalmodal').modal('show');
            }
        });
    };
  $(document).ready(function() {
    $('#form').submit(function(e) {
      e.preventDefault();
      $.ajax({
        type: $(this).attr('method'),
        url: $(this).attr('action'),
        data: new FormData(this),
        processData: false,
        contentType: false,
        success: function(response) {
          if (response.sukses) {
            Swal.fire({
              title: 'Berhasil!',
              text: 'Reservasimu diterima, tunggu diverifikasi ya',
              icon: 'success',
              confirmButton: 'Iya'
            }).then((result) => {
              if (result.isConfirmed) {
                window.location = '<?= base_url('/profile') ?>';
              }
            })
          } else if (response.gagal) {
            Swal.fire({
              title: 'Gagal!',
              text: 'Reservasimu gagal, cek tanggal dan waktu',
              icon: 'error',
              confirmButton: 'Iya'
            })
          } else if (response.error) {
            Swal.fire({
              title: 'Error!',
              text: 'Isi semua field',
              icon: 'error',
              confirmButton: 'Iya'
            })
          }

        },
      });
    });
  });
</script>
